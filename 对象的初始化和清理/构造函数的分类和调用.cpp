#include <iostream>
using namespace std;

//分类
//有参构造函数 无参构造函数
//默认构造函数 拷贝构造函数

class Person
{
//分类
public:
	//无参构造函数(默认构造函数)
	Person() {
		cout << "Person 无参构造函数" << endl;
	}
	//有参构造函数
	Person(int a) {
		cout << "Person 有参构造函数" << endl;
	}
	//拷贝构造函数
	Person(const Person& p) {
		cout << "Person 拷贝构造函数" << endl;
	}
	~Person() {
		cout << "Person 析构函数的调用" << endl;
	}

private:

};

